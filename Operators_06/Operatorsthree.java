public class Operatorsthree {
  public static void main(String[] args) {
    // Comparison Operators
    int x = 5;
    int y = 2;
    System.out.println(x > y);
    System.out.println(x < y);
    System.out.println(x == y);
    System.out.println(x != y);
    System.out.println(x >= y);
    System.out.println(x <= y);

    // Logical Operators
    int z = 5;
    System.out.println(z > 3 && z < 10);
    System.out.println(z > 3 || z < 4);
    System.out.println(!(z > 3 && z < 10));
  }
}